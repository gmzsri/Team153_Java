package day04_matematikselislemler_Increment;

public class C05_Increment {
    public static void main(String[] args) {

        //Increment ( değer artirma)
        // 3 sekilde assignment(atama)  gerçekleştirilir


        int sayi = 10;

        // 1.gösterim sekli

        sayi = sayi + 3;


        //2.gösterim sekli

        sayi += 3;

        // 3. gösterim sekli

        sayi ++ ; //bu gösterim sekli sadece 1 artırır !den fazla artrma yapmaz





    }
}
