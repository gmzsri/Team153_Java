package day04_matematikselislemler_Increment;

public class C06_Decrement {
    public static void main(String[] args) {

        // Decrement ( deger azaltma)
        // 3 farklı sekilde assignment işlemi gercekleştirilir

        int sayi =57 ;

        // 1. assignment şekli

        sayi = sayi - 5;

        // 2. assignmnet sekli

        sayi -= 7;

        // 3. assignment sekli

        sayi -- ; // 1 değer azaltır sadece

        // 3. gösterim sekli sadece değer artırma ve azaltma işleminde kullanılır
        // bölme ve carpma işleminde kullanılmaz


    }
}
